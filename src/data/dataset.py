"""Dataset builders for WRDNet.

Supports:
  - Supervised training on Cityscapes (foggy + clear + depth + labels)
  - Supervised + domain adaptation (Cityscapes + ACDC/Zurich paired)
  - ACDC-only training (real fog with labels)
  - Evaluation on Foggy Driving

Data layout (matches actual downloaded data):
  data/
    cityscapes/
      leftImg8bit_trainvaltest/leftImg8bit/{train,val}/{city}/
      leftImg8bit_trainval_foggyDBF/leftImg8bit_foggyDBF/{train,val}/{city}/
      disparity_trainvaltest/disparity/{train,val}/{city}/
      labels/{train,val}/{city}/  (YOLO .txt, generated by convert script)
    rgb_anon_trainvaltest/rgb_anon/fog/{train,val,test}/{sequence}/
    acdc_labels/{train,val}/{sequence}/  (YOLO .txt, generated by convert script)
    Foggy_Zurich/RGB/{sequence}/
    Foggy_Driving/leftImg8bit/{test,test_extra}/{category}/
    Foggy_Driving/bboxGt/{test,test_extra}/{category}/
"""

import os
from typing import Tuple, Optional

import torch
from torch.utils.data import DataLoader, Dataset, ConcatDataset

from .foggy_cityscapes import FoggyCityscapesDataset
from .acdc import ACDCDataset
from .foggy_zurich import FoggyZurichDataset
from .foggy_driving import FoggyDrivingDataset
from .collate import wrdnet_collate_fn, paired_collate_fn


class PairedDADataset(Dataset):
    """
    Wraps a synthetic dataset and a real dataset for domain adaptation.

    Each __getitem__ returns a dict with both 'synth' and 'real' sub-dicts.
    When datasets have different lengths, the shorter one is cycled.
    """

    def __init__(self, synth_dataset: Dataset, real_dataset: Dataset):
        self.synth_dataset = synth_dataset
        self.real_dataset = real_dataset
        self._len = max(len(synth_dataset), len(real_dataset))

    def __len__(self):
        return self._len

    def __getitem__(self, idx):
        synth_idx = idx % len(self.synth_dataset)
        real_idx = idx % len(self.real_dataset)

        synth_sample = self.synth_dataset[synth_idx]
        real_sample = self.real_dataset[real_idx]

        return {
            'synth': synth_sample,
            'real': real_sample,
        }


def build_dataloaders(config) -> Tuple[DataLoader, Optional[DataLoader]]:
    """
    Build training and validation data loaders.

    Modes:
      1. Supervised only: Cityscapes (foggy + clear + depth + labels)
      2. Supervised + DA: Cityscapes paired with ACDC/Zurich (unlabeled real fog)
      3. ACDC only: Real fog training with labels

    Config keys used:
      data_root: root data directory (default: 'data')
      dataset: primary dataset name (default: 'foggy_cityscapes')
      fog_density: Cityscapes fog density (default: '0.02')
      batch_size: batch size (default: 8)
      num_workers: dataloader workers (default: 4)
      input_size: image size (default: 640)
      use_domain_adaptation: enable DA pairing (default: False)
      use_fda: enable FDA (implies DA)
      use_dct_align: enable DCT alignment (implies DA)
      use_fsg_consistency: enable FSG consistency (implies DA)
      real_datasets: list of real dataset names for DA (default: ['acdc', 'zurich'])

    Args:
        config: Config object with dataset settings
    Returns:
        train_loader, val_loader
    """
    data_root = getattr(config, 'data_root', 'data')
    batch_size = getattr(config, 'batch_size', 8)
    num_workers = getattr(config, 'num_workers', 4)
    input_size = getattr(config, 'input_size', 640)
    fog_density = getattr(config, 'fog_density', '0.02')

    # Determine if domain adaptation is needed
    use_da = (
        getattr(config, 'use_domain_adaptation', False)
        or getattr(config, 'use_fda', False)
        or getattr(config, 'use_dct_align', False)
        or getattr(config, 'use_fsg_consistency', False)
    )

    # ── Build synthetic (labeled) training dataset ──
    synth_train = FoggyCityscapesDataset(
        root=os.path.join(data_root, 'cityscapes'),
        split='train',
        fog_density=fog_density,
        input_size=input_size,
        load_clear=True,
        load_depth=getattr(config, 'use_depth', False),
        config=config,
    )

    # ── Build validation dataset (ACDC val — real fog with labels) ──
    val_dataset = ACDCDataset(
        root=os.path.join(data_root, 'rgb_anon_trainvaltest'),
        split='val',
        labels_dir=os.path.join(data_root, 'acdc_labels', 'val'),
        input_size=input_size,
        config=config,
    )

    # ── Build real (unlabeled) dataset for domain adaptation ──
    if use_da:
        real_dataset_names = getattr(config, 'real_datasets', ['acdc', 'zurich'])
        real_datasets = []

        for name in real_dataset_names:
            if name == 'acdc':
                # ACDC train images (unlabeled — use without labels for DA)
                ds = ACDCDataset(
                    root=os.path.join(data_root, 'rgb_anon_trainvaltest'),
                    split='train',
                    labels_dir=os.path.join(data_root, 'acdc_labels', 'train'),
                    input_size=input_size,
                    config=config,
                )
                if len(ds) > 0:
                    real_datasets.append(ds)
            elif name == 'zurich':
                ds = FoggyZurichDataset(
                    root=os.path.join(data_root, 'Foggy_Zurich'),
                    input_size=input_size,
                    config=config,
                )
                if len(ds) > 0:
                    real_datasets.append(ds)

        if real_datasets:
            real_train = ConcatDataset(real_datasets) if len(real_datasets) > 1 else real_datasets[0]
            print(f"  DA: {len(real_train)} real fog images for domain adaptation")
            train_dataset = PairedDADataset(synth_train, real_train)
            collate = paired_collate_fn
        else:
            print("  WARNING: No real datasets found for DA, falling back to supervised only")
            train_dataset = synth_train
            collate = wrdnet_collate_fn
    else:
        train_dataset = synth_train
        collate = wrdnet_collate_fn

    # ── Build dataloaders ──
    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=True,
        collate_fn=collate,
    )

    val_loader = DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
        collate_fn=wrdnet_collate_fn,
    )

    print(f"  Train: {len(train_dataset)} samples, {len(train_loader)} batches")
    print(f"  Val: {len(val_dataset)} samples, {len(val_loader)} batches")

    return train_loader, val_loader


def build_test_loader(config) -> DataLoader:
    """
    Build test dataloader for Foggy Driving evaluation.

    Args:
        config: Config object
    Returns:
        test_loader for Foggy Driving dataset
    """
    data_root = getattr(config, 'data_root', 'data')
    batch_size = getattr(config, 'batch_size', 8)
    num_workers = getattr(config, 'num_workers', 4)
    input_size = getattr(config, 'input_size', 640)

    test_dataset = FoggyDrivingDataset(
        root=os.path.join(data_root, 'Foggy_Driving'),
        split='all',
        input_size=input_size,
        config=config,
    )

    test_loader = DataLoader(
        test_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
        collate_fn=wrdnet_collate_fn,
    )

    print(f"  Test (Foggy Driving): {len(test_dataset)} samples, {len(test_loader)} batches")

    return test_loader
